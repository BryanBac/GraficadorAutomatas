    public void creartxt(String aux) throws IOException {
        String ruta = "archivo.txt";
        File archivo = new File(ruta);
        BufferedWriter bw;
        if (archivo.exists()) {
            bw = new BufferedWriter(new FileWriter(archivo));
            bw.write(aux);
        } else {
            bw = new BufferedWriter(new FileWriter(archivo));
            bw.write(aux);
        }
        bw.close();

    }

